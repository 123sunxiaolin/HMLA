import Foundation
import UIKit

extension UIColor{
    
    class func BackColor()-> UIColor{
        return UIColor(hexString: "#efeff4")!
    }
    
    class func LineColor()-> UIColor{
        return UIColor(hexString: "#d7d7d7")!
    }
    
}
